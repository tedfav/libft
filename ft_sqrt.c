int		ft_sqrt(int nb)
{
	int	res;
	int	tmp;
	int	cmp;

	res = 0;
	tmp = 1;
	cmp = 1;
	if (nb == 1)
		return (1);
	while (cmp < nb)
	{
		cmp = cmp + tmp;
		res++;
		tmp += 2;
	}
	return (res);
}
